package datatype;

public class TypeCasting {
    public static void main(String[] args) {
        int a = 12400;
        long b = a;
        double d = b;
        byte e = (byte)a;

        System.out.println(a);
        System.out.println(b);
        System.out.println(d);
        System.out.println(e);
    }
}
